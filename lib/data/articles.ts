// Function to get articles by category
export function getArticlesByCategory(category: ArticleCategory): Article[] {
  return sampleArticles.filter(article => article.category === category);
}

// Function to get published articles
export function getPublishedArticles(): Article[] {
  return sampleArticles.filter(article => article.isPublished);
}

// Function to search articles
export function searchArticles(query: string, locale: string = 'en'): Article[] {
  const lowercaseQuery = query.toLowerCase();
  
  return sampleArticles.filter(article => {
    const title = article.title[locale]?.toLowerCase() || '';
    const excerpt = article.excerpt[locale]?.toLowerCase() || '';
    const content = article.content[locale]?.toLowerCase() || '';
    const tags = article.tags.join(' ').toLowerCase();
    
    return title.includes(lowercaseQuery) ||
           excerpt.includes(lowercaseQuery) ||
           content.includes(lowercaseQuery) ||
           tags.includes(lowercaseQuery);
  });
}